export const formatSellerProducts = (products = []) => {
  return products.map((product) => {
    const imageFiles =
      product.media
        ?.filter((item) => item.type?.startsWith("image"))
        .map((item) => item.url) || [];

    return {
      ...product,
      _id: product._id || `seller-${product.id}`,
      images: product.images?.length ? product.images : imageFiles,
      seller: product.seller || { shopName: "Seller" },
      averageRating: product.averageRating || 4.5,
      numReviews: product.numReviews || 0,
      oldPrice: product.oldPrice || Number(product.price) + 200,
      offer: product.offer || 10,
    };
  });
};

export const getAllProducts = () => {
  const saved = localStorage.getItem("seller_products");
  const sellerProducts = saved ? formatSellerProducts(JSON.parse(saved)) : [];

  const merged = [...sellerProducts, ...demoProducts];

  // Prevent duplicate products after refresh
  const seen = new Set();
  return merged.filter((product) => {
    const id = product._id || product.id;
    if (seen.has(id)) return false;
    seen.add(id);
    return true;
  });
};
